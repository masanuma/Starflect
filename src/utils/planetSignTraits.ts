/**
 * 特定の天体×星座の組み合わせの特徴を取得
 */
export const getPlanetSignTrait = (planet: string, sign: string): string => {
  return planetSignTraits[planet]?.[sign] || `${planet}が${sign}座の特徴について、詳しい情報を準備中です。`;
};

/**
 * 天体の基本説明を取得
 */
export const getPlanetMeaning = (planet: string): string => {
  return planetMeanings[planet] || `${planet}の基本的な意味について、詳しい情報を準備中です。`;
};

/**
 * 天体の基本説明 + 星座組み合わせ特徴を取得
 */
export const getPlanetSignDetailWithMeaning = (planet: string, sign: string): string => {
  const meaning = getPlanetMeaning(planet);
  const trait = getPlanetSignTrait(planet, sign);
  
  return `${meaning}\n\n${trait}`;
};

/**
 * 指定された天体の全星座での特徴を取得
 */
export const getPlanetTraitsForAllSigns = (planet: string): Record<string, string> => {
  return planetSignTraits[planet] || {};
};

/**
 * 利用可能な天体のリストを取得
 */
export const getAvailablePlanets = (): string[] => {
  return Object.keys(planetSignTraits);
}; 
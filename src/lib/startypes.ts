export interface StarTypeResult {
  type: StarType
  sunElement: Element
  moonElement: Element
}

/** 太陽と月の黄経から星タイプを判定 */
export function starTypeOf(sunLon: number, moonLon: number): StarTypeResult {
  const sunElement = SIGNS[signIndex(sunLon)].element
  const moonElement = SIGNS[signIndex(moonLon)].element
  return { type: STAR_TYPES[sunElement][moonElement], sunElement, moonElement }
}
